package lista3_4;

public class Endereco {
    private String logrador;
    private int numero;
    private String bairro;
    private String cidade;
    private String estado;

    public Endereco(String logrador, int numero, String bairro, String cidade, String estado) {
        this.logrador = logrador;
        this.numero = numero;
        this.bairro = bairro;
        this.cidade = cidade;
        this.estado = estado;
    }
}
